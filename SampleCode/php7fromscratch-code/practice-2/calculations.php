<?php

/**
 * Calculates Addition of 2 numbers
 *
 * @param $a
 * @param $b
 * @return mixed
 */
function addition($a, $b)
{
    return $a + $b;
}


/**
 * Calculates Subtraction of 2 numbers
 *
 * @param $a
 * @param $b
 * @return mixed
 */
function subtraction($a, $b)
{
    return $a - $b;
}


/**
 * Calculates Multiplication of 2 numbers
 *
 * @param $a
 * @param $b
 * @return mixed
 */
function multiplication($a, $b)
{
    return $a * $b;
}


/**
 * Calculates Division of 2 numbers
 *
 * @param $a
 * @param $b
 * @return float
 */
function division($a, $b)
{
    return $a / $b;
}


/**
 * Calculates Modulo of 2 numbers
 *
 * @param $a
 * @param $b
 * @return int
 */
function modulo($a, $b)
{
    return $a % $b;
}


/**
 * Calculates Power of 2 numbers
 *
 * @param $a
 * @param $b
 * @return mixed
 */
function power($a, $b)
{
    return $a ** $b;
}